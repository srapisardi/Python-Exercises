def cheese_and_crackers(cheese_count, boxes_of_crackers):
    print("You have",cheese_count,"cheeses!")
    print("you have",boxes_of_crackers,"crackers!")

# Integers as arguments
cheese_and_crackers(20,30)

cheese = 10
crackers = 50

# Variables as arguments
cheese_and_crackers(cheese, crackers)

# Math as arguments
cheese_and_crackers(10 + 5, 20 + 20)

# Math using variables
cheese_and_crackers(cheese + 20, crackers + 15)
