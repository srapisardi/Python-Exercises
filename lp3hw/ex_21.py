def add(a,b):
    sum = a + b
    return sum

def subtract(a,b):
    difference = a - b
    return difference

def multiply(a,b):
    product = a * b
    return product

def divide(a,b):
    quotient = a / b
    return quotient

print(add(1,2))
print(subtract(5,2))
print(multiply(6,6))
print(divide(25,5))
